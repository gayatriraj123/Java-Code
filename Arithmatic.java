package Marvellous;

public class Arithmatic
{
    public int value1;
    public int value2;

    public Arithmatic(int A,int B)
    {
        value1=A;
        value2=B;

    }
    public int Addition()
    {
        return value1+value2;
    }
    public int Subtraction()
    {
        return value1-value2;
    }
}